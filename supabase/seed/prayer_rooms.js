async function seed() {
  const supabase = getClient()
  log(`Seeding ${PRAYER_ROOMS.length} prayer rooms...`)

  let inserted = 0
  let skipped = 0

  for (const room of PRAYER_ROOMS) {
    const { error } = await supabase.from('prayer_rooms').upsert(
      {
        name: room.name,
        slug: room.slug,
        location_name: room.location_name,
        floor_unit: room.floor_unit ?? null,
        floor_level: room.floor_level ?? null,
        address: room.address ?? null,
        area: room.area,
        location: room.lat && room.lng ? point(room.lng, room.lat) : null,
        wudu_available: room.wudu_available ?? false,
        gender_separated: room.gender_separated ?? false,
        opening_hours: room.opening_hours ?? null,
        images: [],
        access_instructions: room.access_instructions ?? null,
        venue_type: room.venue_type ?? 'other',
      },
      { onConflict: 'slug', ignoreDuplicates: false }
    )

    if (error) {
      logErr(`prayer room "${room.name}"`, error)
      skipped++
    } else {
      inserted++
    }
  }

  log(`Done: ${inserted} upserted, ${skipped} errors`)
}

seed().catch((err) => {
  logErr('seed failed', err)
  process.exit(1)
})
